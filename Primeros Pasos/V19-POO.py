# Veremos conceptos y terminos de la POO.

# ----- Clase ------
# Aqui se agrupan caracteristicas comunes de un objeto. 
# Tomando de ejemplo un auto, algo comun en todos es el chasis, el volante, etc.

# ----- Instancia -----
# Objetos de un clase con sus caracteristicas.
# Siguieno el ejemplo del auto, podemos tener distintas instancias. Un Ferrari y un Fiat por ejemplo.
# Ambos son instancias de un la clase auto pero con caracteristicas distintas.

# ----- Modularizacion -----
# En la POO es normal tenes varias clases/modulos en un programa.
# Cada clase/modulo se encarga de su funcionalidad y sus caracteristicas.

# ----- Encapsulacion -----
# Las clases deben saber unicamente su comportamiento, no el de las otras, su comportamiento no debe ser
# accecible para otras clases. Para que las clases se puedan comunicar esta los metodos de acceso los cuales
# dan acceso y bridan funcionalidad con ciertas caracteristicas de la clase.

